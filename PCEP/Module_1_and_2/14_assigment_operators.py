'''Assignment Operators'''


x=5 #means x has a value of 5

#For incrementing
x = x + 5 
x += 5
print(x)
#both of these are same

#For decrementing
y = 12
y = y - 2 
y -= 2
print(y)

#both of these are same
z = 3 
z = z * 2
z *= 2
print(z)


#Changing without assigning

x = 200
print(x)
print(x+1)
print(x+1)
print(x-1)
#we have not assigned +1 at any stage. just incremented at that time of print only

