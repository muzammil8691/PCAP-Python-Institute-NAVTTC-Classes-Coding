
#Strip function is used to strip/remove the blank spaces



txt = "     banana     "

x = txt.strip()    #Strips the spaces from both sides
y = txt.lstrip()   #Strips the spaces from left sides
z = txt.rstrip()   ##Strips the spaces from right sides

print("of all fruits", x, "is my favorite")
print("of all fruits", y, "is my favorite")
print("of all fruits", z, "is my favorite")
